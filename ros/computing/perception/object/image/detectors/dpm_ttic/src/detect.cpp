IplImage *ipl_cre_resize(IplImage *IM,int width,int height)
{
	IplImage *R_I = cvCreateImage(cvSize(width,height),IM->depth,IM->nChannels);
	cvResize(IM,R_I);	//resize
	return(R_I);
}


