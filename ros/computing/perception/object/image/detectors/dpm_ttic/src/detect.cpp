//initialize accumulated score
double *ini_ac_score(IplImage *IM)
{
	int L = IM->height*IM->width;
	double *A_SCORE = (double *)calloc(L,sizeof(double));
	for(int ii=0;ii<L;ii++) *(A_SCORE+ii)=-100.0;
	return (A_SCORE);
}

